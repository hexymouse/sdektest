package app.entities;

import javax.persistence.Id;

public class Entity {

    @Id
    private Long id;
    private String name;

    public Entity() {}

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return id + " " + name;
    }
}
